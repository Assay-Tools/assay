"""Load pre-generated evaluation JSON into the database.

This decouples evaluation generation (done by Claude Code agents, any LLM, etc.)
from persistence. The evaluation JSON can be generated by any means — the loader
just takes valid JSON matching the schema and writes it to SQLite.

Usage:
    # Load a single JSON file
    uv run python -m assay.evaluation.loader --file evaluations/resend.json

    # Load all JSON files in a directory
    uv run python -m assay.evaluation.loader --dir evaluations/

    # Load from stdin (piped from another process)
    cat eval.json | uv run python -m assay.evaluation.loader --stdin
"""

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path

from assay.database import SessionLocal, init_db
from assay.evaluation.evaluator import (
    AFScoreComponents,
    ReliabilityScoreComponents,
    SecurityScoreComponents,
    compute_af_score,
    compute_reliability_score,
    compute_security_score,
)
from assay.models import (
    EvaluationRun,
    Package,
    PackageAgentReadiness,
    PackageAuth,
    PackageInterface,
    PackagePerformance,
    PackagePricing,
    PackageRequirements,
    ScoreSnapshot,
)


def load_evaluation(data: dict, db) -> str:
    """Load a single evaluation record into the database.

    Returns the package ID on success, raises on failure.
    """
    pkg_id = data["id"]
    now = datetime.now(timezone.utc)

    # Normalize category to canonical list; unknown → "other"
    from assay.evaluation.discovery import CATEGORIES

    cat_slug = data.get("category")
    if cat_slug:
        cat_slug = cat_slug.lower().strip()
        if cat_slug not in CATEGORIES:
            cat_slug = "other"

    # Create or update package
    pkg = db.query(Package).filter_by(id=pkg_id).first()
    if not pkg:
        pkg = Package(id=pkg_id, name=data.get("name", pkg_id), created_at=now)
        db.add(pkg)

    # Update core fields
    pkg.name = data.get("name", pkg.name)
    pkg.homepage = data.get("homepage", pkg.homepage)
    pkg.repo_url = data.get("repo_url", pkg.repo_url)
    pkg.category_slug = cat_slug
    pkg.subcategories = json.dumps(data.get("subcategories", []))
    pkg.tags = json.dumps(data.get("tags", []))
    pkg.what_it_does = data.get("what_it_does")
    pkg.use_cases = json.dumps(data.get("use_cases", []))
    pkg.not_for = json.dumps(data.get("not_for", []))
    pkg.best_when = data.get("best_when")
    pkg.avoid_when = data.get("avoid_when")
    pkg.alternatives = json.dumps(data.get("alternatives", []))
    pkg.version_evaluated = data.get("version_evaluated")
    pkg.last_evaluated = now
    pkg.updated_at = now
    pkg.status = "evaluated"

    # Interface
    iface_data = data.get("interface", {})
    if iface_data:
        iface = db.query(PackageInterface).filter_by(package_id=pkg_id).first()
        if not iface:
            iface = PackageInterface(package_id=pkg_id)
            db.add(iface)
        iface.has_rest_api = iface_data.get("has_rest_api", False)
        iface.has_graphql = iface_data.get("has_graphql", False)
        iface.has_grpc = iface_data.get("has_grpc", False)
        iface.has_mcp_server = iface_data.get("has_mcp_server", False)
        iface.mcp_server_url = iface_data.get("mcp_server_url")
        iface.has_sdk = iface_data.get("has_sdk", False)
        iface.sdk_languages = json.dumps(iface_data.get("sdk_languages", []))
        iface.openapi_spec_url = iface_data.get("openapi_spec_url")
        iface.webhooks = iface_data.get("webhooks", False)

    # Auth
    auth_data = data.get("auth", {})
    if auth_data:
        auth = db.query(PackageAuth).filter_by(package_id=pkg_id).first()
        if not auth:
            auth = PackageAuth(package_id=pkg_id)
            db.add(auth)
        auth.methods = json.dumps(auth_data.get("methods", []))
        auth.oauth = auth_data.get("oauth", False)
        auth.scopes = auth_data.get("scopes", False)
        auth.notes = auth_data.get("notes")

    # Pricing
    pricing_data = data.get("pricing", {})
    if pricing_data:
        pricing = db.query(PackagePricing).filter_by(package_id=pkg_id).first()
        if not pricing:
            pricing = PackagePricing(package_id=pkg_id)
            db.add(pricing)
        pricing.model = pricing_data.get("model")
        pricing.free_tier_exists = pricing_data.get("free_tier_exists", False)
        ftl = pricing_data.get("free_tier_limits")
        pricing.free_tier_limits = json.dumps(ftl) if ftl else None
        pt = pricing_data.get("paid_tiers")
        pricing.paid_tiers = json.dumps(pt) if pt else None
        pricing.requires_credit_card = pricing_data.get("requires_credit_card", False)
        ewc = pricing_data.get("estimated_workload_costs")
        pricing.estimated_workload_costs = json.dumps(ewc) if ewc else None
        pricing.notes = pricing_data.get("notes")

    # Performance
    perf_data = data.get("performance", {})
    if perf_data:
        perf = db.query(PackagePerformance).filter_by(package_id=pkg_id).first()
        if not perf:
            perf = PackagePerformance(package_id=pkg_id)
            db.add(perf)
        perf.latency_p50_ms = perf_data.get("latency_p50_ms")
        perf.latency_p99_ms = perf_data.get("latency_p99_ms")
        perf.uptime_sla_percent = perf_data.get("uptime_sla_percent")
        rl = perf_data.get("rate_limits")
        perf.rate_limits = json.dumps(rl) if rl else None
        perf.data_source = perf_data.get("data_source", "llm_estimated")
        perf.measured_on = perf_data.get("measured_on")

    # Requirements
    req_data = data.get("requirements", {})
    if req_data:
        req = db.query(PackageRequirements).filter_by(package_id=pkg_id).first()
        if not req:
            req = PackageRequirements(package_id=pkg_id)
            db.add(req)
        req.requires_signup = req_data.get("requires_signup", False)
        req.requires_credit_card = req_data.get("requires_credit_card", False)
        req.domain_verification = req_data.get("domain_verification", False)
        req.data_residency = json.dumps(req_data.get("data_residency", []))
        req.compliance = json.dumps(req_data.get("compliance", []))
        req.min_contract = req_data.get("min_contract", "none")

    # Agent Readiness
    ar_data = data.get("agent_readiness", {})
    if ar_data:
        ar = db.query(PackageAgentReadiness).filter_by(package_id=pkg_id).first()
        if not ar:
            ar = PackageAgentReadiness(package_id=pkg_id)
            db.add(ar)
        ar.mcp_server_quality = ar_data.get("mcp_server_quality")
        ar.mcp_security_score = ar_data.get("mcp_security_score")
        ar.documentation_accuracy = ar_data.get("documentation_accuracy")
        ar.error_message_quality = ar_data.get("error_message_quality")
        ar.error_message_notes = ar_data.get("error_message_notes")
        ar.idempotency_support = ar_data.get("idempotency_support")
        ar.idempotency_notes = ar_data.get("idempotency_notes")
        ar.pagination_style = ar_data.get("pagination_style")
        ar.retry_guidance_documented = ar_data.get("retry_guidance_documented")
        ar.known_agent_gotchas = json.dumps(ar_data.get("known_agent_gotchas", []))

    # AF Score — compute from af_score_components if present, else legacy fields
    afc_data = data.get("af_score_components")
    if afc_data:
        afc = AFScoreComponents(**afc_data)
        af_score = compute_af_score(afc)
    elif ar_data:
        # Legacy: build AF from old flat fields in agent_readiness
        afc = AFScoreComponents(
            mcp_score=ar_data.get("mcp_server_quality") or 0,
            api_doc_score=ar_data.get("documentation_accuracy") or 0,
            error_handling_score=ar_data.get("error_message_score") or 50,
            auth_complexity_score=ar_data.get("auth_complexity_score") or 50,
            rate_limit_clarity_score=ar_data.get("rate_limit_score") or 50,
        )
        af_score = compute_af_score(afc)
    else:
        af_score = None

    # Security Score — compute from sub-components if present, else use top-level
    sec_data = data.get("security_score_components")
    if sec_data:
        sec = SecurityScoreComponents(**sec_data)
        security_score = compute_security_score(sec)
        if ar_data:
            ar.tls_enforcement = sec.tls_enforcement
            ar.auth_strength = sec.auth_strength
            ar.scope_granularity = sec.scope_granularity
            ar.dependency_hygiene = sec.dependency_hygiene
            ar.secret_handling = sec.secret_handling
            ar.security_notes = sec.security_notes
    else:
        # Legacy: use top-level security_score from agent_readiness if present
        security_score = ar_data.get("security_score") if ar_data else None

    # Reliability Score — compute from sub-components if present
    rel_data = data.get("reliability_score_components")
    if rel_data:
        rel = ReliabilityScoreComponents(**rel_data)
        reliability_score = compute_reliability_score(rel)
        if ar_data:
            ar.uptime_documented = rel.uptime_documented
            ar.version_stability = rel.version_stability
            ar.breaking_changes_history = rel.breaking_changes_history
            ar.error_recovery = rel.error_recovery
    else:
        reliability_score = None

    # Persist composite scores
    if ar_data:
        if af_score is not None:
            ar.af_score = af_score
        if security_score is not None:
            ar.security_score = security_score
        if reliability_score is not None:
            ar.reliability_score = reliability_score
        # Also persist AF sub-component scores to agent_readiness
        if afc_data:
            ar.auth_complexity = afc_data.get("auth_complexity_score")
            ar.rate_limit_clarity = afc_data.get("rate_limit_clarity_score")
    if af_score is not None:
        pkg.af_score = af_score
    if security_score is not None:
        pkg.security_score = security_score
    if reliability_score is not None:
        pkg.reliability_score = reliability_score

    # Update MCP flag on package interface
    if iface_data.get("has_mcp_server"):
        pkg.af_score = pkg.af_score  # ensure it's set

    # Evaluation audit record
    eval_run = EvaluationRun(
        package_id=pkg_id,
        model_used="claude-code-subscription",
        raw_output=json.dumps(data),
        af_score_computed=pkg.af_score,
    )
    db.add(eval_run)
    db.flush()  # get eval_run.id before commit

    # Score history snapshot
    if pkg.af_score is not None:
        snapshot = ScoreSnapshot(
            package_id=pkg_id,
            af_score=pkg.af_score,
            security_score=pkg.security_score,
            reliability_score=pkg.reliability_score,
            evaluation_run_id=eval_run.id,
        )
        db.add(snapshot)

    db.commit()
    return pkg_id


def load_file(filepath: Path, db) -> tuple[int, int]:
    """Load evaluations from a JSON file. Returns (success_count, error_count)."""
    with open(filepath) as f:
        data = json.load(f)

    # Support both single object and array of objects
    if isinstance(data, list):
        items = data
    else:
        items = [data]

    success = 0
    errors = 0
    for item in items:
        try:
            pkg_id = load_evaluation(item, db)
            af = item.get('agent_readiness', {}).get('af_score', 'n/a')
            print(f"  Loaded: {pkg_id} (AF: {af})")
            success += 1
        except Exception as e:
            pkg_id = item.get("id", "unknown")
            print(f"  ERROR loading {pkg_id}: {e}")
            db.rollback()
            errors += 1

    return success, errors


def main():
    parser = argparse.ArgumentParser(description="Load evaluation JSON into Assay database")
    parser.add_argument("--file", type=str, help="Path to a single JSON file")
    parser.add_argument("--dir", type=str, help="Path to directory of JSON files")
    parser.add_argument("--stdin", action="store_true", help="Read JSON from stdin")
    args = parser.parse_args()

    if not any([args.file, args.dir, args.stdin]):
        parser.print_help()
        sys.exit(1)

    init_db()
    db = SessionLocal()

    total_success = 0
    total_errors = 0

    try:
        if args.stdin:
            data = json.load(sys.stdin)
            items = data if isinstance(data, list) else [data]
            for item in items:
                try:
                    pkg_id = load_evaluation(item, db)
                    print(f"  Loaded: {pkg_id}")
                    total_success += 1
                except Exception as e:
                    print(f"  ERROR: {e}")
                    db.rollback()
                    total_errors += 1

        elif args.file:
            filepath = Path(args.file)
            if not filepath.exists():
                print(f"File not found: {filepath}")
                sys.exit(1)
            total_success, total_errors = load_file(filepath, db)

        elif args.dir:
            dirpath = Path(args.dir)
            if not dirpath.is_dir():
                print(f"Directory not found: {dirpath}")
                sys.exit(1)
            for filepath in sorted(dirpath.glob("*.json")):
                print(f"Loading {filepath.name}...")
                s, e = load_file(filepath, db)
                total_success += s
                total_errors += e

    finally:
        db.close()

    print(f"\nDone: {total_success} loaded, {total_errors} errors")


if __name__ == "__main__":
    main()
